    private void TableFilling() {
    DefaultTableModel model = (DefaultTableModel) encounter_history.getModel();
        model.setRowCount(0);
        for(Encounter en : ecoSystem.getEncounterHistory().getEncounterHistory())
             
            {
                if(patient.getEmailId().equals(en.getPatient().getEmailId())&&patient.getPassword().equals(en.getPatient().getPassword()))
           {
                
            Object[] row = new Object[11];
            row[0] = en.getEncounter_ID();
            row[1] = en.getEncounterDate();
            row[2] = en.getDiagnosis();
            row[3] = en.getDoctor().getFirstName()+""+en.getDoctor().getLastName();
            row[4] = en.getVitalSign().getBodyTemp();
            row[5] = en.getVitalSign().getPulseRate();
            row[6] = en.getVitalSign().getRespiratoryRate();
            row[7] = en.getVitalSign().getBloodPressure();
            row[8] = en.getVitalSign().getOxygenSaturation();
            row[9] = en.getVitalSign().getWeight();
            row[10] = en.getVitalSign().getHeight();
            model.addRow(row);
            }}
    
    }
