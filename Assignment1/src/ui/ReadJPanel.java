    private void TableFilling(){
        SimpleDateFormat sanju = new SimpleDateFormat("MMM d, y");
        DefaultTableModel model = (DefaultTableModel) jEmployeeTable.getModel();
        model.setRowCount(0);
        for (ProductDetails p : historyArrayList.getHistoryArrayList()){
            Object[] lines = new Object[10];
            lines[0] = p.getName();
            lines[1] = p.getEmployee_ID();
            lines[2] = p.getAge();
            lines[3] = p.getGender();
            lines[4] = sanju.format(p.getStart_Date());
            lines[5] = p.getLevel();
            lines[6] = p.getTeam_info();
            lines[7] = p.getPosition_Title();
            lines[8] = p.getPhone_Number();
            lines[9] = p.getEmail();
            model.addRow(lines);
            
        }
    }

